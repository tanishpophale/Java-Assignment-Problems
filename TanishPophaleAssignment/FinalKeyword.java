// Final keyword wih example

// Whenever we use a final keyword, for that we cannot change the value i.e it remains constant.

public class FinalKeyword {
 
        public static void main(String[] args){
            final int a = 10;

            System.out.println("The value of final integer variable is: " + a);

            // a = 20;     // It will give an error

        }
}
