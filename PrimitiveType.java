public class PrimitiveType {
    
    public static void main(String[] args) {
        int a = 12;
        float b = 12.3f;
        char c = 'a';
        double d = 45.2356;
        boolean e = true;
        long f = 12154;
        byte g = 4;

        System.out.println("The value of integer variable a is: " + a);
        System.out.println("The value of float variable b is: " + b);
        System.out.println("The value of char variable c is: " + c);
        System.out.println("The value of double variable d is: " + d);
        System.out.println("The value of boolean variable e is: " + e);
        System.out.println("The value of long variable f is: " + f);
        System.out.println("The value of byte varibale g is: " + g);
    }
}


